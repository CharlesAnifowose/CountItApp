import Foundation
import UIKit

extension UIColor{
    
    static func clickerRedColor() -> UIColor{
        
        return UIColor(red: 249, green: 5, blue: 105, alpha: 1)
        
    }
    
    static func clickerGreenColor() -> UIColor{
        
        return UIColor(red: 119, green: 244, blue: 8, alpha: 1)
        
    }
    
    
    static func clickerBlueColor() -> UIColor{
        
        return UIColor(red: 24, green: 190, blue: 213, alpha: 1)
        
    }
    
}
